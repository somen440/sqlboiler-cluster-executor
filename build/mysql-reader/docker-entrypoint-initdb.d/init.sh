#!/bin/bash

MYSQL_PWD=root mysql -u root -v mysql <<SQL
  RESET MASTER;
SQL

MYSQL_PWD=root mysql -u root -v <<SQL
  CHANGE MASTER TO
    MASTER_HOST = '$MYSQL_REPLICATION_HOST',
    MASTER_USER = '$MYSQL_REPLICATION_USER',
    MASTER_PASSWORD = '$MYSQL_REPLICATION_PASSWORD',
    MASTER_AUTO_POSITION = 1,
    MASTER_DELAY = 10;
  START SLAVE;
SQL
